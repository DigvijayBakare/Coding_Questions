package TCS_NQT.String;

// Que: Find the ASCII value of a character

public class ASCIIValue {
    public static void main(String[] args) {
        // declare a character and print it
        char a = 'a';
        System.out.println("The character: " + a);

        // print the ascii value for the character
        System.out.println("The ASCII value of the character " + a + " is " + (int) a);
    }
}
